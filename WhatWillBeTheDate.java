
public class WhatWillBeTheDate {

	public static void main(String[] args) {
		// (6 + 10) % 7 is 2		
		// If today is Tuesday, what will be the day in 100 days?
		
		int day;
		
		day = (2 + 100) % 7;
		System.out.println("Sunday = 0, Monday = 1, Tuesday = 2, Wednesday = 3, Thursday = 4, Friday = 5, Saturday = 6, Sunday = 7");
		System.out.println(day);
		
	}

}
