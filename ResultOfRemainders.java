
public class ResultOfRemainders {

	public static void main(String[] args) {
		int remainder;
		
		remainder = 56 % 6;
		System.out.println(remainder);
		remainder = 78 % -4;
		System.out.println(remainder);
		remainder = -34 % 5;
		System.out.println(remainder);
		remainder = -34 % -5;
		System.out.println(remainder);
		remainder = 5 % 1;
		System.out.println(remainder);
		remainder = 1 % 5;
		System.out.println(remainder);

	}

}
